/*
Module  : Training
Layer   : TARGET

Purpose:
Load employee training activities from STG_TRAINING
into FACT_ACTIVITY.
*/

INSERT INTO FACT_ACTIVITY
(
    EMPLOYEE_KEY,
    DATE_KEY,
    ACTIVITY_TYPE_KEY,
    PROJECT_KEY,
    TRAINING_KEY,
    HOURS_WORKED,
    SOURCE_SYSTEM,
    LOAD_TIMESTAMP
)

SELECT

    DE.EMPLOYEE_KEY,

    DD.DATE_KEY,

    DAT.ACTIVITY_TYPE_KEY,

    NULL AS PROJECT_KEY,

    DT.TRAINING_KEY,

    STG.DURATION_HOURS AS HOURS_WORKED,

    'TRAINING' AS SOURCE_SYSTEM,

    SYSTIMESTAMP

FROM STG_TRAINING STG

INNER JOIN DIM_EMPLOYEE DE
    ON STG.EMPLOYEE_ID = DE.EMPLOYEE_ID

INNER JOIN DIM_DATE DD
    ON STG.TRAINING_DATE = DD.FULL_DATE

INNER JOIN DIM_TRAINING DT
    ON STG.TRAINING_NAME = DT.TRAINING_NAME

INNER JOIN DIM_ACTIVITY_TYPE DAT
    ON DAT.ACTIVITY_TYPE_NAME = 'TRAINING'

WHERE NOT EXISTS
(
    SELECT 1
    FROM FACT_ACTIVITY FA
    WHERE FA.EMPLOYEE_KEY = DE.EMPLOYEE_KEY
      AND FA.DATE_KEY = DD.DATE_KEY
      AND FA.ACTIVITY_TYPE_KEY = DAT.ACTIVITY_TYPE_KEY
      AND FA.TRAINING_KEY = DT.TRAINING_KEY
      AND NVL(FA.HOURS_WORKED,-1) = NVL(STG.DURATION_HOURS,-1)
);